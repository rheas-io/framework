"use strict";
// This package does nothing specific. It's a wrapper for installing
// all the rheas module for creating an appication.
//
// Rheas framework is a collection of more than ten independent modules
// Stating all these modules on the application package will make it cluttered
// and it is possible that we will forget to add some important modules.
//
// This package solves that issue. This package installs all the rheas modules
// as dependencies and all that developers has to do is to instal this package
// using `npm install @rheas/framework`
